#include "libft.h"
#include <stdio.h>
#include <string.h>
#include <stdlib.h>

/* renvoie un pointeur sur la première occurrence
 du caractère c dans la chaîne s.  */

int	ft_strchr(const char *s, int c)
{
	int		i;

	if (!s)
		return (0);
	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			return (i);
		i++;
	}
	return (0);
}
